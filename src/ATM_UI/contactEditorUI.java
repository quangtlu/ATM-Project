    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String t=jButton1.getText();
        if(t.equals("登陆")){
            if(ipAddress==null){
                showMessage.showmessage("网络设置未完成");
            }else{
                if(jTextField1.getText()==null||jPasswordField1.getText()==null){
                    showMessage.showmessage("未输入用户名或密码");
                }
             if(Clicent.Clicent==null){ try {
            /* Create and display the form */
            jframe.setEnabled(false);
            Clicent.connectToSever(ipAddress, portNumber);
            Clicent.getStreams();
            jframe.setEnabled(true);
        }catch(ConnectException e){
            showMessage.showmessage("无法连上服务器");
            jframe.setEnabled(true);
        } catch (IOException ex) {
            Logger.getLogger(contactEditorUI.class.getName()).log(Level.SEVERE, null, ex);
        }}
                Clicent.SendData(new login(jTextField1.getText(),Security.encrypt(jPasswordField1.getText())));
                Object tem= Clicent.ReceiveData();
                if(tem instanceof login){
                    login receive=(login)tem;
                    System.out.println(receive.getStatus());
                   if(receive.getStatus()){
                       jLabel19.setText(Security.decryptyString( receive.getMoney()));
                jPanel1.setVisible(false);
                jTextField1.setText("");
                jPasswordField1.setText("");
                jPanel3.setVisible(true);
                timeCounter();
                currentPanel=jPanel3;
                }if(!receive.getStatus()){
                    if(receive.getMoney().equals("NO_SUCH_USER"))
                       showMessage.showmessage("账号错误");
                   if(receive.getMoney().equals( "USER_CHECKED"))
                       showMessage.showmessage("用户已登录");
                   if(receive.getMoney().equals( "USER_LOCKED"))
                       showMessage.showmessage("用户已锁");
                   if(receive.getMoney().equals( "WRONG_PASSWORD"))
                       showMessage.showmessage("密码错误");
                   if(receive.getMoney().equals( "WRONG_PASSWORD_FIANL"))
                       showMessage.showmessage("密码错误&锁死");
                }
                }
            } 
        }
        if(t.equals("保存")){
            ipAddress=jTextField1.getText();
            try{
            portNumber=(int)Double.parseDouble(jPasswordField1.getText());
            }catch(NumberFormatException e){
                System.out.println("端口号设置错误");
            }
            try {
                jframe.setEnabled(false);
                Clicent.connectToSever(ipAddress, portNumber);
                Clicent.getStreams();
                jframe.setEnabled(true);
            }catch(SocketException e){
                showMessage.showmessage("服务器异常");
            } catch (IOException ex) {
                Logger.getLogger(contactEditorUI.class.getName()).log(Level.SEVERE, null, ex);
            }
                
            showMessage.showmessage("已设置");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

